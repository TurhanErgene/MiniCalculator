    private void btnLikaMedActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLikaMedActionPerformed
      try
        {   arithmetic_operation();
            jLabel1.setText("");
         }
      
      catch (java.lang.NumberFormatException e) {
           JOptionPane.showMessageDialog(null,"Du måste ange andra tal."
                ,"Felmeddelande",JOptionPane.ERROR_MESSAGE);
      }
      //sparar ange tal inom decimal
      double decimal = Double.parseDouble(String.valueOf(txfDisplay.getText()));
      //jämför decimal till rullat av decimal och om det är inte oändlig 
        if ((decimal == Math.floor(decimal)) && !Double.isInfinite(decimal))
        {   //vi rullar och sätter decimal som int
            txfDisplay.setText((int)(decimal) + " ");
        }
        else { //eller som är en decimal om det krävs
            txfDisplay.setText(decimal + " ");
        }
    }//GEN-LAST:event_btnLikaMedActionPerformed

