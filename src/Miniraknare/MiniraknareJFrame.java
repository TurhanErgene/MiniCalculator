    public void arithmetic_operation(){
    
        switch(calculation){
            
        case 1 :  // addition +
            ans = num + Double.parseDouble(txfDisplay.getText());
            txfDisplay.setText(Double.toString(ans));
        break;
        
        case 2 :  // subtraktion -
            ans = num - Double.parseDouble(txfDisplay.getText());
            txfDisplay.setText(Double.toString(ans));
        break;
        
        case 3 :   // multiplikation *
            ans = num * Double.parseDouble(txfDisplay.getText());
            txfDisplay.setText(Double.toString(ans));
        break;
        
        case 4 :   // division /
            ans = num / Double.parseDouble(txfDisplay.getText());
            txfDisplay.setText(Double.toString(ans));
        break;
            
        case 5 :  // x^y 
           ans = Math.pow(num,Double.parseDouble(txfDisplay.getText()));
           txfDisplay.setText(String.valueOf(ans));
        break;
        }
        
    }
       
    
